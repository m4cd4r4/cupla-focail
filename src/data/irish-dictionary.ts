export interface DictionaryEntry {
  id: string;
  irish: string;
  english: string;
  englishAlt?: string[];
  partOfSpeech: PartOfSpeech;
  category: DictionaryCategory;
  gender?: 'masculine' | 'feminine';
  searchTerms: string[];
}

function normalize(text: string): string {
  return text
    .toLowerCase()
    .replace(/á/g, 'a')
    .replace(/é/g, 'e')
    .replace(/í/g, 'i')
    .replace(/ó/g, 'o')
    .replace(/ú/g, 'u');
}

function entry(
  id: string,
  irish: string,
  english: string,
  partOfSpeech: PartOfSpeech,
  category: DictionaryCategory,
  opts?: { englishAlt?: string[]; gender?: 'masculine' | 'feminine' },
): DictionaryEntry {
  const terms = [normalize(irish), normalize(english)];
  if (opts?.englishAlt) {
    for (const alt of opts.englishAlt) terms.push(normalize(alt));
  }
  return { id, irish, english, partOfSpeech, category, searchTerms: terms, ...opts };
}

